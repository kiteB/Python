# 정수형 계산
# 덧셈
print(4 + 7)

# 뺄셈
print(2 - 4)

# 곱셈
print(5 * 3)

# 나머지
print(7 % 3)

# 거듭제곱
print(2 ** 3)

# 소수형 계산
print(4.0 + 7.0)
print(2.0 - 4.0)
print(5.0 * 3.0)
print(7.0 % 3.0)
print(2.0 ** 3.0)

# 정수 + 소수

print(4.0 + 7)
print(2.0 - 4)
print(5 * 3.0)
print(7 % 3.0)
print(2.0 ** 3)

# 나눗셈 -> 항상 결과 소수!

print(7 / 2)
print(6 / 2)
print(7.0 / 2)
print(6.0 / 2.0)

# 사칙연산
print(2 * (2 + 3))