# standard library
import math     # 기본 모듈
print(math.log10(100))
print(math.cos(0))
print(math.pi)

import random   # 랜덤한 값을 만들어내고 싶을 때 사용
print(random.random())

import os       # 파이썬으로 운영체제를 조작하기 위한 함수
print(os.getlogin())    # 어떤 계정으로 로그인되어있는지 알려줌
print(os.getcwd())      # 현재 이 파일이 있는 폴더의 경로를 알려줌

